                    <h6>Roles and Functions of Lead Country</h6>
                    <ol>
                        <li>Where sub-sectors have been identified, the designated Chair Countries will seek to identify specific projects for facilitating cooperation.</li>
                        <li>The Chair Country will function in consultation with an Expert Group drawn from member countries.</li>
                        <li>The Chair Country will chair and coordinate the work of the Expert Group, and perform secretarial functions if the Expert Group in such a way as to ensure the
                            satisfactory and successful implementation of the projects and to monitor the progress of the work.
                        </li>
                        <li>The members of the Expert Group will be in regular contact through correspondence, fax, e-mail, etc. and will meet whenever deemed necessary.</li>
                        <li>The Expert Group will coordinate, monitor and review progress in projects being implemented and will report on the same to the Sectoral Committee.</li>
                        <li>The Expert Group may invite such representatives from the public/private sector and regional/international organizations to its meetings whenever deemed appropriate.
                        </li>
                        <li>The sub-sector Chair Country position will rotate between members every three years.</li>
                    </ol>

