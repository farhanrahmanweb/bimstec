                    <p>Transport and Communication is one of the 14 sectors of cooperation since the inception of BIMSTEC in 1997. This sector is led by India.</p>

                    <p>BIMSTEC Members have recognized the importance to create air, sea, and land linkages among the Member States. The 1st Meeting of the BIMSTEC Expert Group on Transport and
                        Communication Sector held in April 2001, in New Delhi discussed various relevant issues, including Transportation and Cross-border Facilitation, Multimodal Transport and
                        Logistics, Infrastructure Development, Aviation, Maritime Transport, HRD, as well as Communication Linkages and Networking.</p>

                    <p>The Tenth Session of the BIMSTEC Senior Officials’ Meeting held in Dhaka on 18 December, 2005 approved, for implementation, the ADB proposal to conduct a BIMSTEC Transport
                        Logistics Study. Accordingly, Asian Development Bank (ADB) carried out a study on Transport and Infrastructure aspects in the BIMSTEC Member States, which was completed in
                        2007. The BIMSTEC Transport Infrastructure and Logistic Study (BTILS) was further updated and enhanced by ADB in 2014. The Study has identified 167 projects which will
                        enhance the connectivity in the Bay of Bengal region at an estimated cost of USD 45-50 billion. Out of 167 projects, the Study has prioritized 66. One of the
                        recommendations of the Study was to create a single Working Group on transport and trade facilitation to be referred as BIMSTEC Transport Connectivity Working Group
                        (BTCWG).</p>

                    <p>The Inception Meeting of the BIMSTEC Transport and Connectivity Working Group (BTCWG) held in Bangkok, Thailand in August 2016 reviewed the key agreements reached at the
                        Final BTILS Workshop, covering updated and enhanced policies/strategies, priority projects, monitoring framework, action plan, theme and institutional arrangements and
                        formulated the draft Terms of Reference (ToR) of the BTCWG and monitoring system for the projects.</p>

                    <p>BIMSTEC Leaders during the Goa Retreat in October 2016 agreed to explore the possibility of having a BIMSTEC Motor Vehicle Agreement. The Leaders also agreed to prepare a
                        Master Plan for BIMSTEC connectivity and also to conclude BIMSTEC Coastal Shipping Agreement. The Second Meeting of the BIMSTEC Transport Connectivity Working Group held in
                        Bangkok, Thailand on 13-14 November 2017 finalised the modalities of drafting the BIMSTEC Master Plan on Transport Connectivity with technical assistance of ADB.</p>

                    <p>First Working Group Meeting to discuss and finalise the Draft Text of the Coastal Shipping Agreement among BIMSTEC Member States, which was drafted by India, was held in New
                        Delhi on 27-28 November 2017.</p>

                    <p>First Meeting of the BIMSTEC Working Group to negotiate the Draft Test of the Motor Vehicles Agreement for the Regulation of Passenger and Cargo Vehicular Traffic between
                        and among BIMSTEC Member Countries, which was drafted by India, was held in New Delhi, India on 09-10 April 2018.</p>

                    <p>During the Fourth BIMSTEC Summit held in Kathmandu, Nepal on 30-31 August 2018, the Leaders of the BIMSTEC Member States reiterated their resolve to establish seamless
                        multi-modal transportation linkages and smooth, synchronized and simplified transit facilities through the development, expansion and modernization of highways, railways,
                        waterways, sea routes, airways in the region and directed to speed up efforts to conclude the BIMSTEC Coastal Shipping Agreement and the BIMSTEC Motor Vehicle Agreement as
                        early as possible taking into account the special circumstances and needs of the Member States.</p>

                    <p>The Leaders also noted with satisfaction the preparation of the draft BIMSTEC Master Plan on Transport Connectivity and called for its early adoption. The Leaders agreed
                        that the Master Plan would serve as a strategic document that guides actions and promotes synergy among various connectivity frameworks, such as the ASEAN Master Plan on
                        Connectivity 2025 (MPAC 2025), the Ayeyawady – Chao Phraya- Mekong Economic Cooperation Strategy (ACMECS), to achieve enhanced connectivity and sustainable development in
                        the region.</p>

                    <p>The Leaders in the Fourth BIMSTEC Summit also decided to establish a Working Group to deal with information technology and communications related matters with a view to
                        provide greater access, more affordable and high-speed internet and mobile communications to the peoples of the region.</p>

                    <p>One Workshop was held in Bangkok, Thailand on 17-18 September 2018 to discuss the progress made on the BIMSTEC Master Plan on Transport Connectivity. The Third Meeting of
                        the BIMSTEC Transport Connectivity Working Group is expected to be held in 2019 to discuss and finalise the BIMSTEC Master Plan on Transport Connectivity.</p>

