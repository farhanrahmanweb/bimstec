                        <li>Third STEOM – 6 March 2003, Colombo, Sri Lanka</li>
                        <li>Fourth STEOM – 2004, Bangkok, Thailand</li>
                    </ul>
                    <p>The Fifth Meeting of the Senior Trade and Economic Officials is scheduled to be hosted by Bangladesh. At the 17th Session of the BIMSTEC Senior Officials Meeting (SOM) held
                        in Kathmandu on 07 Feb 2017, the Delegation of Bangladesh stated that the dates for the Fifth STEOM and the Sixth BIMSTEC TEMM will be notified upon completion of the
                        ongoing negotiations to conclude a number of Constituent Agreements of the BIMSTEC FTA.</p>

                    <p><strong>Trade Negotiating Committee (TNC) and FTA Negotiations</strong></p>

                    <p>The Framework Agreement on the BIMSTEC Free Trade Area was signed in Phuket, Thailand on 08 February 2004 and came into force on 30 June 2004. Article 2 of the Framework
                        Agreement outlines the following areas to be covered by the “Comprehensive Free Trade Area (FTA)”:</p>

                    <ul class="list-style-3">
                        <li>Progressive elimination of tariffs and non-tariff barriers in substantially all trade in goods</li>
                        <li>Progressive liberalization of trade in services with substantial sectoral coverage</li>
                        <li>Establishing an open and competitive investment regime that facilitates and promotes investment within the BIMSTEC FTA</li>
                        <li>Establishing effective trade and investment facilitating measures, including, but not limited to, simplification of customs procedures and development of mutual
                            recognition arrangements
                        </li>
                        <li>Establishing appropriate mechanisms for implementation of the Agreement</li>
                    </ul>

                    <p>Article 10 of the Framework Agreement provided for the establishment of BIMSTEC Trade Negotiating Committee (BIMSTEC TNC) to carry out the program of negotiations as set out
                        in the Framework Agreement. Accordingly, the BIMSTEC TNC was created in 2004 (under the permanent Chairmanship of Sri Lanka) which has since held 20 rounds of negotiations,
                        the last one held in Khon Kaen, Thailand on 07-09 September 2015. While TNC itself focuses on negotiations on the Agreement on Trade in Goods, pursuant to Article 10.2 of
                        the Framework Agreement, it has created a number of Working Groups to assist in the negotiations:</p>


                    <ol type="i">
                        <li>Working Group on Rules of Origin (17th meeting held in Khon Kaen, Thailand on 07-09 Sept 2015);</li>
                        <li>Working Group on Trade in Services (7th meeting held in Khon Kaen, Thailand on 07-09 Sept 2015);</li>
                        <li>Working Group on Investment (7th meeting held in Khon Kaen, Thailand on 07-09 Sept 2015);</li>
                        <li>Working Group on Legal Experts (17th meeting held in Khon Kaen, Thailand on 07-09 Sept 2015);</li>
                        <li>Working Group on Customs Cooperation (1st meeting was held on 10-11 May 2018 in New Delhi, India);</li>
                        <li>Working Group on Trade Facilitation (1st Meeting is proposed to be held in Dhaka, Bangladesh in 2018)</li>
                    </ol>


                    <p>The TNC is currently engaged in the negotiations to conclude the following Constituent Agreements/Protocols which would form integral part of the BIMSTEC FTA:</p>

                    <ol type="i">
                        <li>Agreement on Trade in Goods of the Framework Agreement on the BIMSTEC FTA</li>
                        <li>Agreement on Trade in Services of the Framework Agreement on the BIMSTEC FTA</li>
                        <li>Agreement on Investment of the Framework Agreement on the BIMSTEC FTA</li>
                        <li>Agreement on Cooperation and Mutual Assistance in Customs Matters for the BIMSTEC FTA</li>
                        <li>Agreement on Rules of Origin and Operational Certification Procedures for the BIMSTEC FTA</li>
                        <li>Agreement on Trade Facilitation for the BIMSTEC FTA</li>
                        <li>Protocol to amend the Framework Agreement on the BIMSTEC FTA</li>
                    </ol>
                    <p>The 21st Meeting of the BISMTEC TNC is scheduled to be held in Dhaka, Bangladesh in mid-2018, when a number of Constituent Agreements of BIMSTEC FTA are expected to be
                        finalized. It is hoped that these Agreements, in particular the Agreement on Trade in Goods would be ready to be signed by the next Trade and Economic Ministers’ Meeting
                        (TEMM), in order to trigger the operationalization of the BIMSTEC FTA.</p>


                    <p><strong> Sector participation in BIMSTEC Economic Cooperation</p></strong>

                    <p>BIMSTEC Business Forum (BBF) and BIMSTEC Economic Forum (BEF) are the two forums that allow active participation of the private sector representatives meet and discuss
                        various issues related to their interest. In the BBF, private sector representatives from the BIMSTEC Member States meet and discuss various issues. Results from the BBF
                        are submitted to the BIMSTEC Economic Forum (BEF) where the private sectors have an opportunity to discuss freely with representatives from the public sector. The BEF
                        reports the outcome of its Meetings to the STEOM.
                    </p>

                    <p>The BIMSTEC Business Forum (BBF) was formed by the representatives of private sector of BIMSTEC Member States at a meeting organized by ESCAP in Bangkok, Thailand in March
                        1998 to enhance private sector cooperation among the Member States of the BIMSTEC region in identified sectors, sub-sectors and projects. BBF held seven (7) meetings
                        between 2003 and 2016, the last meeting held in New Delhi, India on 21 July 2016. The Eighth Meeting of the BIMSTEC Business Forum is proposed to be held in Dhaka,
                        Bangladesh in 2018.</p>

