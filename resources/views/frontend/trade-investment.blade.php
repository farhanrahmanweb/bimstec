                    <p>The Second BIMST-EC Ministerial Meeting held in Dhaka, Bangladesh on 19 December 1998 emphasized on the need for establishing a BIMSTEC Economic Forum (BEF) to promote
                        Government-Private sector cooperation among the Member States. Accordingly, the process of formulation of BIMSTEC Economic Forum was initiated by Bangladesh through a
                        Preparatory Meeting on the formation of the BIMST-EC Economic Forum which was held in Dhaka in November 1999. Later, the Economic Forum met formally for the first time in
                        New Delhi, India on 03 July 2000. Both the Government Officials (from the Foreign Ministry as well as the Trade/Economic Ministry) and Private sectors (Members of the
                        BIMSTEC Business Forum) from each of the BIMSTEC Member State are the participants of the BEF. BEF have held six meetings. The Sixth Meeting of the BEF held in Bangkok,
                        Thailand on 24 February 2011 agreed that the Seventh Meeting of BEF will be held in Bangladesh back-to-back with the STEOM and TEMM.</p>

