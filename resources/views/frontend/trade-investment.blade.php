                    <p>Trade and Investment was one of the initial six sectors of BIMSTEC cooperation as identified by the 2nd BIMSTEC Ministerial Meeting held in Dhaka, Bangladesh on 19 December
                        1998. Bangladesh acts as the Lead Country for this sector.

                    <p>The initial vision for BIMSTEC economic cooperation, as articulated in the 1st (Special) Ministerial Meeting held in Bangkok on 22 December 1997, had three major pillars: i)
                        close public-private partnership to promote economic cooperation; ii) identification of sectors, sub-sectors and projects for economic cooperation, and iii) elimination of
                        non-tariff and tariff barriers through government-to-government negotiations.

                    <p>Trade and Investment Sector has a well developed institutional mechanism to conduct its activities: i) Trade and Economic Ministerial Meeting (TEMM); ii) Senior Trade and
                        Economic Officials’ Meeting (STEOM); iii) Trade Negotiating Committee (TNC) and its Working Groups; iv) BIMSTEC Economic Forum; and v) BIMSTEC Business Forum.

                    <p><strong>Trade and Economic Ministerial Meeting (TEMM)</strong></p>

                    <p>The Trade and Economic Ministerial Meeting (TEMM) consists of the Trade/Economic Ministers of the Member States. To date, five TEMMs have taken place:</p>

                    <ul class="list-style-3">
                        <li>First Meeting of BIMSTEC TEMM – 07 August 1998, Bangkok, Thailand</li>
                        <li>Second Meeting of BIMSTEC TEMM – 27 April 2000 in New Delhi, India</li>
                        <li>Third Meeting of BIMSTEC TEMM – 15 Feb 2001, Yangon, Myanmar</li>
                        <li>Fourth Meeting of BIMSTEC TEMM – 7 March 2003, Colombo, Sri Lanka</li>
                        <li>Fifth Meeting of BIMSTEC TEMM – 7 Feb 2004, Phuket, Thailand</li>

                    </ul>

                    <p>The Sixth Meeting of BIMSTEC TEMM is scheduled to be held in Dhaka, Bangladesh. At the 17th Session of the Senior Officials Meeting (SOM) held in Kathmandu on 07 Feb 2017,
                        the Delegation of Bangladesh stated that the dates for the Fifth BIMSTEC Senior Trade/Economic Officials Meeting (STOEM) and the Sixth BIMSTEC TEMM will be notified upon
                        completion of the [1]three agreements and one protocol related to BIMSTEC FTA under negotiation.</p>

                    <p><strong>Senior Trade/Economic Officials’ Meetings (STEOM)</strong></p>
                    <p>The Senior Trade and Economic Officials’ Meeting (STEOM) is an operational body comprising Senior Officials of the Trade/Commerce Ministry and representative from Ministry
                        of Foreign Affairs of the Member States. Tasks belonging to the STEOM are the negotiation of the BIMSTEC FTA,cooperation in the Trade and Investment Sector and the
                        preparation for the TEMM. STOEM reports to the TEMM. To date, the Senior Trade and Economic Officials of BIMSTEC countries have met four times:</p>

                    <p style="font-size: 11px;">[1] Trade in Goods; Agreement on Dispute Settlement Procedures and Mechanism; Agreement on Cooperation and Mutual Assistance in Customs Matters;
                        Protocol to Amend the Framework Agreement on BIMSTEC FTA.</p>

