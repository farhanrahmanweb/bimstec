                    <h5 class="alt-font font-weight-700 text-extra-dark-gray margin-20px-bottom margin-four-top text-uppercase">Policy-Making Bodies</h5>

                    <h6 class="alt-font font-weight-700 text-extra-dark-gray margin-20px-bottom margin-four-top text-uppercase">1. BIMSTEC Summit</h6>
                    <p>This is the highest policy making body in the BIMSTEC process. As per the decision of the 6th BIMSTEC Ministerial Meeting held in Thailand on 8th February 2004, the Summit
                        should be held every two years, as possible. The First Summit Meeting of the Heads of the BIMSTEC Countries was held in Bangkok, Thailand on 31 July 2004. The Second Summit
