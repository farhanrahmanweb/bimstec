                    <h5 class="alt-font font-weight-700 text-extra-dark-gray margin-20px-bottom margin-four-top text-uppercase">Business Forum and Economic Forum</h5>
                    <p>Two forums under the STEOM allow active participation of the private sector. In the Business Forum, private sector representatives from the BIMSTEC Member States meet and
                        discuss various issues. Results from the Business Forum will be forwarded to Economic Forum where the private sector have an opportunity to discuss freely with
                        representatives from the public sector from Member States. The Economic Forum will then report outcomes of its meetings to the STEOM. The last Forum was held in New
