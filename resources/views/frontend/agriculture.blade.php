                        <li>Strengthening of Agriculture Statistical Information System and its networks;</li>

                        <li>Workshop on Good Agricultural Practices (GAP);</li>

                        <li>The development of Agricultural bio-technology including bio-safety;</li>

                        <li>Preventing and Control of trans-boundary animal diseases;</li>

                        <li>Affiliation of agricultural universities, veterinary universities, and agricultural research institutes;</li>

                        <li>Development of seed sector in BIMSTEC Member States;</li>

                        <li>Workshop(s) on improving Agricultural Trade and Investment.</li>
                    </ul>

